public class multipleTable {
    public static void main(String[] args) {
       int n=8;
       int mul=1;
       int mulSum=0;
       for(int i=1; i<=10; i++){
        mul=n*i;
        mulSum= mulSum+mul;
        System.out.println(mul);
       } 

       System.out.println(mulSum);    
        
       }
       
    
}
